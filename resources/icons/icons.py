    @staticmethod
    def get_themed_icon(icon_name: str, color: str, size: QSize = QSize(24, 24)) -> QIcon:
        """
        Get an icon with the specified color.
        
        Args:
            icon_name: The name of the icon
            color: The color to apply to the icon
            size: The size of the icon
            
        Returns:
            QIcon: The themed icon
        """
        svg_content = IconProvider._svg_icons.get(icon_name)
        if not svg_content:
            # Return an empty icon if the icon name is not found
            return QIcon()
        
        # Replace the color in the SVG content
        svg_content = svg_content.replace('stroke="currentColor"', f'stroke="{color}"')
        
        # Create a pixmap from the SVG content
        renderer = QSvgRenderer(bytes(svg_content, encoding='utf-8'))
        pixmap = QPixmap(size)
        pixmap.fill(Qt.GlobalColor.transparent)
        
        painter = QPainter(pixmap)
        renderer.render(painter)
        painter.end()
        
        return QIcon(pixmap)
